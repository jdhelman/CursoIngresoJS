/*
Debemos lograr tomar un nombre con 'prompt' 
y luego mostrarlo por 'alert' al presionar el botón  'mostrar'*/
function mostrar()
{
	let nombre = prompt("Ingrese su nombre"); //declaro la variable nombre y la inicializo con un prompt que permitirá ingresar un dato que se asignará a la variable nombre
    
	alert(nombre);
}

